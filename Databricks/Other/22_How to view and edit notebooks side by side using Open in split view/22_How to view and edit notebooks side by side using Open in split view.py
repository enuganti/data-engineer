# MAGIC #### Edit notebooks side by side
# MAGIC - To **view and edit notebooks side by side**, click **Column split icon** to **add a split view** of the current notebook.
# MAGIC - You can also **drag a notebook tab and drop** it to the **right** to **edit notebooks side by side**.
# MAGIC - Useful for **comparing code and results or copying code** from **one notebook to another**.

# COMMAND ----------

# MAGIC %md
# MAGIC ##### **❌ Limitations of "Open in Split View":**
# MAGIC
# MAGIC - The **split view** for notebooks (not just cells) **only supports vertical** side-by-side layout.
# MAGIC - You **cannot split** two notebooks **horizontally (top-bottom)** using the UI.
# MAGIC - You can only **drag a second notebook** to the **left or right side** of the screen for **vertical splitting**.

# COMMAND ----------

# MAGIC %md
# MAGIC | Task                                       | Supported? | Notes                         |
# MAGIC | ------------------------------------------ | ---------- | ----------------------------- |
# MAGIC | Vertical split (side-by-side)              | ✅          | Between notebooks or cells    |
# MAGIC | Horizontal split (top-bottom)              | ❌          | Not available                 |
# MAGIC | Editing two notebooks side by side         | ✅          | Drag notebook tabs left/right |

# COMMAND ----------

